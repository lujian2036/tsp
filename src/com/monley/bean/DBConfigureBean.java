package com.monley.bean;

public class DBConfigureBean {
	
	private String DataBase_IP;
	private String DataBase_Port;
	private String DataBase_Name;
	private String DataBase_User;
	private String DataBase_Pwd;
	
	public String getDataBase_IP() {
		return DataBase_IP;
	}
	public void setDataBase_IP(String dataBase_IP) {
		DataBase_IP = dataBase_IP;
	}
	public String getDataBase_Port() {
		return DataBase_Port;
	}
	public void setDataBase_Port(String dataBase_Port) {
		DataBase_Port = dataBase_Port;
	}
	public String getDataBase_Name() {
		return DataBase_Name;
	}
	public void setDataBase_Name(String dataBase_Name) {
		DataBase_Name = dataBase_Name;
	}
	public String getDataBase_User() {
		return DataBase_User;
	}
	public void setDataBase_User(String dataBase_User) {
		DataBase_User = dataBase_User;
	}
	public String getDataBase_Pwd() {
		return DataBase_Pwd;
	}
	public void setDataBase_Pwd(String dataBase_Pwd) {
		DataBase_Pwd = dataBase_Pwd;
	}

	
}
